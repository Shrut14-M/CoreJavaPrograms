package com.basicPrograms;

public class Var {
	
	public static void main(String args[]) {
		
		// Primitive datatypes
		
		byte a = 45;                //1 byte
		short b = 500;              // 2 byte   1 byte= 8 bits
		int c = 456778666;          // 4 byte
		long d = 345678879L;        // 8 byte
		float e = 23.56f;           // 4 byte
		double f = 23.45566564332245665435546545d;  //  8 byte
		char g = 'g';               // 2 byte
		boolean h = true;           // 1 bit    default value is False668
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		
		
		// Non Primitive datatypes
		
		String s= "shruti";
				System.out.println(s);			
		
	}

}
